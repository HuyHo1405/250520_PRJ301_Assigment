package model.dto;

/**
 *
 * @author Admin
 */
public class OrderStatusDTO {
    private int id;
    private String status;

    public OrderStatusDTO() {
    }

    public OrderStatusDTO(int id, String status) {
        this.id = id;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
